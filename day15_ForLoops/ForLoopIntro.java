package day15_ForLoops;

public class ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World".repeat(100));

        System.out.println("------------------------------------------");

        for (int i=1; i <=10; i++) {

            // pre or post increment does not matter.

            System.out.println("Hello World!");

        }
    }
}
